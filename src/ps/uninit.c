#include <main.h>

void	ps_uninit(t_ps *ps)
{
	uint32_t	i;

	i = 0;
	while (i < ps->tk_len)
	{
		free(ps->tk[i].str);
		i += 1;
	}
	free(ps->tk);
	ps->tk = NULL;
}
