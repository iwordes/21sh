#include <main.h>

bool	ps_is_ri2(const char *tk)
{
	ITER(tk, ft_isdigit(*tk));
	tk += (*tk == '<');
	tk += (*tk == '<');
	if (*tk == '&')
	{
		tk += 1;
		ITER(tk, ft_isdigit(*tk));
	}
	return (*tk == 0 || ft_isspace(*tk));
}
