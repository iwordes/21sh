#include <sh.h>

void	sh_right(void)
{
	g_sh.cx += 1;
	if (g_sh.cx < g_sh.cols)
		tm_right();
	else
	{
		write(1, "\n", 1);
		g_sh.cy += 1;
		g_sh.cx = 0;
	}
}
