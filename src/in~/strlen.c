#include <sh.h>

size_t	in_strlen(const char *in)
{
	size_t	l;

	l = 0;
	while (*in != 0)
	{
		if (ft_iscntrl(*in))
			l += 2;
		else
			l += 1;
		in += 1;
	}
	return (l);
}
