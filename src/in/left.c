#include <sh.h>

#define VAL_CNT(C) (C != 0 && ft_iscntrl(C))

void	in_left(char **cmd, size_t *i, size_t *l)
{
	if (*i > 0)
	{
		if (VAL_CNT((*cmd)[*i - 1]))
			tm_left();
		tm_left();
		*i -= 1;
	}
	(void)l;
}
