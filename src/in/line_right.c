#include <main.h>

void	in_line_right(t_in *in)
{
	if (in->x < in->ln[in->y].len)
	{
		in->x += 1;
		if (in->s == 0)
			tm_right();
		else
		{
			if (in->s > 0)
				in->x += in->s - 1;
			in->s = 0;
			in_redraw(in);
		}
	}
}
